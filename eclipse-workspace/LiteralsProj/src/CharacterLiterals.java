
public class CharacterLiterals {
	public static void main(String [] args) {
		System.out.println('a');
		
		System.out.print("->");
		System.out.print('\n'); 
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\t'); 
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\\');
		System.out.println("<-");
		
		System.out.print("->");
		System.out.print('\''); 
		System.out.println("<-");
		
		//unicode values
		System.out.println('\u0041'); 
		System.out.println('\u096A'); 
		System.out.println('\u23F3'); 
		System.out.println('\u20B9'); 
	}
}
